import 'package:flutter/material.dart';

TextStyle textDesign(int size) {
  return TextStyle(
    fontSize: size.toDouble(),
    color: Colors.white,
    fontFamily: "Cabin",
  );
}
TextStyle textDesignbold(int size) {
  return TextStyle(
    fontSize: size.toDouble(),
    color: Colors.white,
    fontFamily: "Cabin",
    fontWeight: FontWeight.bold,
  );
}
